#ADD operator addition of two numbers.
def add(n1,n2):
    return(n1+n2)
print(add(10,20))

#SUB operator subtraction of two numbers.
def sub(n1,n2):
    return(n1-n2)
print(sub(10,20))

#mul operator multiplication of two numbers.
def mul(n1,n2):
    return(n1*n2)
print(mul(10,20))

#div operator division of two numbers.
def div(n1,n2):
    return(n1/n2)
print(div(10,20))

#modules operator moduler division of two numbers.
def modules(n1,n2):
    return(n1%n2)
print(modules(10,20))

#Exponentiation operator power of two numbers.
def exp(n1,n2):
    return(n1**n2)
print(exp(10,20))

#Floor division operator
def flodiv(n1,n2):
    return(n1//n2)
print(flodiv(10,20))


